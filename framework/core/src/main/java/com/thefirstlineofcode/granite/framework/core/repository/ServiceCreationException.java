package com.thefirstlineofcode.granite.framework.core.repository;

public class ServiceCreationException extends Exception {
	private static final long serialVersionUID = 4670531182902880655L;

	public ServiceCreationException() {
		super();
	}

	public ServiceCreationException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ServiceCreationException(String arg0) {
		super(arg0);
	}

	public ServiceCreationException(Throwable arg0) {
		super(arg0);
	}

}
