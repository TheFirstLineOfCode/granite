package com.thefirstlineofcode.granite.framework.core.pipeline.stages.routing;

public class RoutingRegistrationException extends Exception {
	private static final long serialVersionUID = 3811699307357030778L;

	public RoutingRegistrationException() {
		super();
	}

	public RoutingRegistrationException(String message, Throwable cause) {
		super(message, cause);
	}

	public RoutingRegistrationException(String message) {
		super(message);
	}

	public RoutingRegistrationException(Throwable cause) {
		super(cause);
	}	
	
}
