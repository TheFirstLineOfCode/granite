CREATE TABLE OFFLINE_MESSAGES(id VARCHAR(64) PRIMARY KEY, message_id VARCHAR(64) NOT NULL, message VARCHAR(16384) NOT NULL, jid VARCHAR(2047) NOT NULL, create_time TIMESTAMP DEFAULT CURRENT_TIMESTAMP);
CREATE UNIQUE INDEX UNIQUE_INDEX_OFFLINE_MESSAGES_MESSAGE_ID ON OFFLINE_MESSAGES (message_id);
CREATE INIDX INDEX_OFFLINE_MESSAGES_JID OFFLINE_MESSAGES (jid);
