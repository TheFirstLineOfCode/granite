package com.thefirstlineofcode.granite.xeps.muc;

public class ReduplicateRoomException extends Exception {
	private static final long serialVersionUID = -8764239393518879879L;

	public ReduplicateRoomException() {
		super();
	}

	public ReduplicateRoomException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public ReduplicateRoomException(String arg0) {
		super(arg0);
	}

	public ReduplicateRoomException(Throwable arg0) {
		super(arg0);
	}
	
}
